/* ══════════════════════════════════════════════
   applyLang(langCode)
   Call this on every page after DOM is ready.
   It reads all [data-i18n] elements and fills them.
   Also saves the choice to localStorage.
   ══════════════════════════════════════════════ */
function applyLang(lang) {
  if (!lang || !I18N[lang]) lang = 'en';
  localStorage.setItem('abcbank_lang', lang);
  const dict = I18N[lang];

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  // Update lang select if present
  const sel = document.getElementById('lang-select');
  if (sel) sel.value = lang;

  // Update html lang attribute
  document.documentElement.lang = lang;
}

